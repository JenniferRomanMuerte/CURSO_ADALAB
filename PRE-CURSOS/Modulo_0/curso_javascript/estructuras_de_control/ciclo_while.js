// Declaración de una variable para controlar el ciclo
let contador = 0;

// Mientras el contador sea menor que 5, ejecuta el bloque de código
while (contador < 5) {
    console.log(`Iteración ${contador}`);
    contador++;
    }
// Cuando el ciclo termina ( contador sea mayor que 5), se ejecuta esta línea
console.log('Bucle terminado');